package kurut0_Calculator;

public class Operations {

    static Float mult(Float a, Float b) {
        return a * b;
    }

    static Float div(Float a, Float b) {
        return a / b;
    }

    static Float add(Float a, Float b) {
        return a + b;
    }

    static Float sub(Float a, Float b) {
        return a - b;
    }
}
